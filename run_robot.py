"""
run_robot.py  —  hand-maintained driver (NOT auto-generated)
===================================================================
Set ROBOT_ID below, then launch via ./run_robot.sh (macOS: that sets
DYLD_LIBRARY_PATH so the Vicon dylibs load).

Runtime inputs:
  - path_<ROBOT_ID>_robot.csv   waypoints in the robot's local frame
                                (regenerated by compute_path.py / the website)
  - run_config_<ROBOT_ID>.json  per-robot constants
                                (regenerated by compute_path.py / the website)
  - vicon_config.json           Vicon host + subject names
                                (hand-maintained, NOT regenerated)

Toggles (edit here):
  - WRAP_MODE    "arc" | "navigate"
  - USE_VICON    True  -> between-wrap pose correction from Vicon
  - MOCK_ROBOT   True  -> simulate the Create3 with mock_robot.MockCreate3
                          (no Bluetooth, no Vicon SDK needed)

Correction strategy (Phase B):
  Before the first wrap of a pole AND after the last wrap of a pole,
  read Vicon, compare to the expected waypoint, and if off by more than
  the threshold, issue turn_* + move to close the gap, then a final turn
  to align heading with the next leg. No correction on intermediate
  wrap waypoints.
"""

import csv, json, math, os, time

# ==================================================================
# ROBOT SELECTION / MODE  (edit these)
# ==================================================================
ROBOT_ID   = "A"
WRAP_MODE  = "arc"         # "arc" | "navigate"
USE_VICON  = True         # False -> skip all Vicon reads + corrections
MOCK_ROBOT = True         # True  -> MockCreate3 instead of real Bluetooth Create3

# ==================================================================
# CONDITIONAL IMPORTS (Bluetooth/Create3 vs mock)
# ==================================================================
if MOCK_ROBOT:
    from mock_robot import MockCreate3, event
    Bluetooth = None
    _RealCreate3 = None
else:
    from irobot_edu_sdk.backend.bluetooth import Bluetooth
    from irobot_edu_sdk.robots import event, Create3 as _RealCreate3
    MockCreate3 = None

# ==================================================================
# LOADERS
# ==================================================================
_HERE = os.path.dirname(os.path.abspath(__file__))

def load_run_config(rid):
    with open(os.path.join(_HERE, "run_config_" + rid + ".json")) as f:
        cfg = json.load(f)
    cfg["light_rgb"] = tuple(cfg["light_rgb"])
    return cfg

def load_path(csv_name):
    pts = []
    with open(os.path.join(_HERE, csv_name), newline="") as f:
        for row in csv.DictReader(f):
            pts.append({
                "x":       float(row["x"]),
                "y":       float(row["y"]),
                "type":    row["type"],
                "pole":    int(row["pole"]),
                "arc_deg": float(row["arc_deg"]),
            })
    return pts

def load_vicon_config():
    path = os.path.join(_HERE, "vicon_config.json")
    if not os.path.exists(path):
        raise FileNotFoundError(
            "vicon_config.json not found — required when USE_VICON=True. "
            "Create it with host + subjects, or set USE_VICON=False."
        )
    with open(path) as f:
        return json.load(f)

CFG  = load_run_config(ROBOT_ID)
PATH = load_path(CFG["path_csv"])

# ==================================================================
# POSE SOURCES  (Vicon or mock-robot pose, in robot-local cm + deg)
# ==================================================================

def _yaw_from_quat(qx, qy, qz, qw):
    """Yaw (rotation around Z) in degrees, assuming Vicon Z is up."""
    siny_cosp = 2.0 * (qw * qz + qx * qy)
    cosy_cosp = 1.0 - 2.0 * (qy * qy + qz * qz)
    return math.degrees(math.atan2(siny_cosp, cosy_cosp))

def _wrap180(a):
    while a >  180.0: a -= 360.0
    while a <= -180.0: a += 360.0
    return a


class ViconPoseSource:
    """Reads from a running ViconClient and converts to the robot's
    local frame (cm, heading in degrees with 90° = forward, matching
    the SDK's reset_navigation grid)."""

    def __init__(self, client, subject, start_pos_mm, start_yaw_deg):
        self.client = client
        self.subject = subject
        self.sx_mm = float(start_pos_mm[0])
        self.sy_mm = float(start_pos_mm[1])
        self.start_yaw = float(start_yaw_deg)
        # Rotation to apply so that the robot's startup facing direction
        # ends up as the local +y axis (SDK heading = 90°).
        self.rot_deg = 90.0 - self.start_yaw
        self._rot_rad = math.radians(self.rot_deg)
        self._cos = math.cos(self._rot_rad)
        self._sin = math.sin(self._rot_rad)

    def get_pose_local(self):
        frame = self.client.latest_frame
        if frame is None:
            return None
        body = frame.subject(self.subject)
        if body is None or body.occluded:
            return None
        dx_mm = float(body.position[0]) - self.sx_mm
        dy_mm = float(body.position[1]) - self.sy_mm
        x_local_mm = self._cos * dx_mm - self._sin * dy_mm
        y_local_mm = self._sin * dx_mm + self._cos * dy_mm
        qx, qy, qz, qw = body.rotation_quat
        yaw = _yaw_from_quat(qx, qy, qz, qw)
        h_local = _wrap180(yaw + self.rot_deg)
        return (x_local_mm / 10.0, y_local_mm / 10.0, h_local)


class MockPoseSource:
    """Returns the mock robot's own simulated pose — perfect tracking.
    (MockCreate3 already uses SDK convention: reset -> heading 90°.)"""

    def __init__(self, mock_robot):
        self._mock = mock_robot

    def get_pose_local(self):
        return (self._mock.x, self._mock.y, self._mock.heading)


